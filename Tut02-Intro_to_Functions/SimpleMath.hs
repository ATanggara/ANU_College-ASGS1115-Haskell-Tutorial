-- Binary Boolean Operations
True || True
True || False
False || True
False || False

True && True
True && False
False && True
False && False

-- Unary Boolean Operations
not True
not False

-- Sum of 5 values:
s x1 x2 x3 x4 x5 = x1+x2+x3+x4+x5

-- Average of 5 values:
average x1 x2 x3 x4 x5 = (s x1 x2 x3 x4 x5)/5


